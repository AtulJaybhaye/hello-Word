package Inheritance;

public class Student {
	
	int id;
	String name;
	String CollegeName;
	String Branch;
	Address address;
	
	Student(int id,String name,String CollegeName,String Branch,Address address)
	{
		this.id=id;
		this.name=name;
		this.CollegeName=CollegeName;
		this.Branch=Branch;
		this.address=address;
	}

}
